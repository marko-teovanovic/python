#List sort .sort()
cars = ['bmw', 'audi', 'opel', 'toyota', 'ford', 'subaru']
cars.sort()
print(cars)

#Reverse sort
cars = ['bmw', 'audi', 'opel', 'toyota', 'ford', 'subaru']
cars.sort(reverse=True)
print(cars)

#.sorted() function
cars = ['bmw', 'audi', 'opel', 'toyota', 'ford', 'subaru']
print("Here is the original list:")
print(cars)
print("nHere is the sorted list:")
print(sorted(cars))

#.reverse() function
cars = ['bmw', 'audi', 'opel', 'toyota', 'ford', 'subaru']
print(cars)

cars.reverse()
print(cars)
